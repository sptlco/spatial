export const Balance = createElement<typeof Container>((props, ref) => {
  const periods = ["1d", "1w", "1m", "3m", "ytd", "1y", "5y", "all"];

  const [period, setPeriod] = useState(periods[0]);

  const market = useSWR("platform/logistics/ticker/market", Spatial.market.current);
  const { data, isLoading } = useSWR("platform/logistics/assets/balance", Spatial.market.balance, {
    refreshInterval: 30000, // only poll every 30s
    dedupingInterval: 15000
  });

  const balance = data && !data.error ? data.data : undefined;
  const ethereum = market.data && !market.data.error ? market.data.data : undefined;

  return (
    <Container {...props} ref={ref} className={clsx("flex flex-col p-10 gap-10", props.className)}>
      <Container className="flex items-start justify-between gap-10">
        <Container className="flex flex-col gap-6">
          <H2 className="text-2xl font-bold">Balance</H2>
          {isLoading ? <Span /> : <Span className="text-5xl font-bold">{formatCurrency(Number(balance) * (ethereum?.current_price ?? 0))}</Span>}
        </Container>
        <ToggleGroup.Root
          type="single"
          value={period}
          onValueChange={(value) => {
            if (value) setPeriod(value);
          }}
          className="rounded-lg bg-background-subtle flex items-center overflow-hidden"
        >
          {periods.map((period, i) => (
            <ToggleGroup.Item
              key={i}
              value={period}
              className="px-5 py-2 uppercase font-extrabold text-hint rounded-lg data-[state=on]:bg-button data-[state=on]:text-foreground-primary"
            >
              {period}
            </ToggleGroup.Item>
          ))}
        </ToggleGroup.Root>
      </Container>
      <ResponsiveContainer className="bg-grid" height={512}>
        <Container className="relative h-full w-full">
          <Container className="absolute inset-0 pointer-events-none size-full" />
          <LineChart accessibilityLayer data={chartData} margin={{ left: 12, right: 12 }}>
            <Line dataKey="desktop" type="linear" stroke="var(--color-green)" strokeWidth={2} dot={false} activeDot={false} />
            <Tooltip
              content={() => null}
              cursor={{
                stroke: "var(--color-green)",
                strokeWidth: 2,
                strokeDasharray: "3 6"
              }}
            />
          </LineChart>
        </Container>
      </ResponsiveContainer>
    </Container>
  );
});

function formatCurrency(value?: number) {
  if (value == null) {
    return "-";
  }

  return new Intl.NumberFormat("en-US", {
    style: "currency",
    currency: "USD",
    currencyDisplay: "code",
    currencySign: "accounting",
    maximumFractionDigits: 2
  }).format(value);
}
